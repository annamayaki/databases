    public viewInterviewFrame(Connection conn, String user) {   
        this.conn = conn;
        this.user = user;
        try {
                 Statement stmt;
                 stmt = conn.createStatement();
                 String query = "select job from interview "+
                         "inner join job on job.id=interview.job "+
                         "where job.submission_date<current_date "+
                         "and job.recruiter='"+user+"';";
                 ResultSet res;
                 res = stmt.executeQuery(query);
                 intJobs = new ArrayList<Integer>();
                 while (res.next()) {
                     intJobs.add(res.getInt(1));
                 }
                 res.close();
                 stmt.close();
                 
                 
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Failed to retrieve critical information. Please try again.");
            Logger.getLogger(viewInterviewFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        initComponents();
    }

  
