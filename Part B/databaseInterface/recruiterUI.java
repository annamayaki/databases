    public recruiterUI(Connection conn, String user, String pass) {
        this.user=user;
        this.conn = conn;
        this.pass = pass;
        this.welcomeMssg = "Welcome, recruiter "+user;
        try {
                 Statement stmt;
                 stmt = conn.createStatement();
                 String query = "select id from job "+
                                "where recruiter='"+user+"';";
                 ResultSet res;
                 res = stmt.executeQuery(query);
                 jobIds = new ArrayList<Integer>();
                 while (res.next()) {
                     jobIds.add(res.getInt(1));
                 }
                 res.close();
                 stmt.close();
                 
                 
        } catch (SQLException ex) {
            Logger.getLogger(viewInterviewFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        initComponents();
    }

    
