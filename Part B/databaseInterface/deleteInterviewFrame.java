    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
          format.parse(startTime);
      } catch (ParseException ex) {
          Logger.getLogger(updateInerviewFrame.class.getName()).log(Level.SEVERE, null, ex);
      }
        Statement stmt;
        try {
          
          stmt = conn.createStatement();
          String query = "delete from interview where Start_time='"+startTime+"' and candidate_username='"+username+"';"; 
          stmt.executeUpdate(query);
          stmt.close();
          JOptionPane.showMessageDialog(rootPane, "Interview deleted successfully.");
      } catch (SQLException ex) {
          JOptionPane.showMessageDialog(rootPane, "Failed to delete interview.");
          Logger.getLogger(updateInerviewFrame.class.getName()).log(Level.SEVERE, null, ex);
      }
    }//GEN-LAST:event_jButton2ActionPerformed

   
