    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (intJobs.contains(id)){
            try {
          format.parse(startTime);
        } catch (ParseException ex) {
          JOptionPane.showMessageDialog(rootPane, "Please insert a valid date.");
          Logger.getLogger(updateInerviewFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
            Statement stmt;
            try {

              stmt = conn.createStatement();
              String query = "insert into interview values "
                      + "('"+dur+"', '"+startTime+"' , '"+pers+"' , '"+comments+"' , '"+username+"' , '"+id+"');";
              stmt.executeUpdate(query);
              stmt.close();
              JOptionPane.showMessageDialog(rootPane, "Interview created successfully.");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Failed to create interview.");
                Logger.getLogger(updateInerviewFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else {
            JOptionPane.showMessageDialog(rootPane, "Please insert a valid job id.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

