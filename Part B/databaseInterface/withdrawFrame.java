    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        idStr = jTextField1.getText();
        if (!idStr.equals("")){
            delId = Integer.parseInt(idStr);
        
            try {
                Statement stmt = conn.createStatement();
                String query1 = "delete from applies where cand_usrname='"+user
                               +"' and job_id="+delId+";";
                stmt.executeUpdate(query1);
                stmt.close();
                
                JOptionPane.showMessageDialog(rootPane, "Appliction withdrawn successfully.");
            } catch (SQLException ex) {
                Logger.getLogger(applyFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(rootPane, "Appliction withdrawal failed.");
            }
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Please select a valid job ID.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

