    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (intJobs.contains(id)){   
            Statement stmt;
            try {

              stmt = conn.createStatement();
              String query = "update applies set education= '"+edu+"' , experience= '"+exp+"'  where "
                      + "cand_usrname= '"+username+"' and job_id= '"+id+"' ;" ;
              stmt.executeUpdate(query);
              stmt.close();
              JOptionPane.showMessageDialog(rootPane, "Scores updated successfully.");
          } catch (SQLException ex) {
              JOptionPane.showMessageDialog(rootPane, "Failed to update scores.");
              Logger.getLogger(updateInerviewFrame.class.getName()).log(Level.SEVERE, null, ex);
          }
        }
        else {
            JOptionPane.showMessageDialog(rootPane, "Please insert a valid job id.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    

